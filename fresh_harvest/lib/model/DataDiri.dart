class DataDiri {
   String _name  = '';
   String _email  = '';
   String _password  = '';
   String _address  = '';
  
    String get name {
    return _name;
  }

  set name(String name) {
    _name = name;
  }
  
  
    String get email {
    return _email;
  }

  set email(String email) {
    _email = email;
  }
  
  
    String get password {
    return _password;
  }

  set password(String password) {
    _password = password;
  }
  
  
    String get address {
    return _address;
  }

  set address(String address) {
    _address = address;
  }
  
  
}
